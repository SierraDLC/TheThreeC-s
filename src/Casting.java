public class Casting {
    public static void main(String[] args) {

        double dubz = 112.35;
        
        int nubz = (int)dubz;

        System.out.println("Double: " + dubz);
        System.out.println("Int: " + nubz);

        String text = "49";
        
        int numz = Integer.parseInt(text);

        
        System.out.println("The string value is: " + text);
        
        System.out.println("The interger value is: " + numz);


    }
}